type Props = Partial<Augment> & { name: string };

export class Augment {
  name: string;
  bp: number;
  hp: number;
  pp: number;
  melee: number;
  ranged: number;
  technique: number;
  floor: number;
  dmgRes: number;
  burn: number;
  freeze: number;
  shock: number;
  blind: number;
  panic: number;
  poison: number;
  stun: number;

  constructor({
    name,
    bp,
    hp,
    pp,
    melee,
    ranged,
    technique,
    floor,
    dmgRes,
    burn,
    freeze,
    shock,
    blind,
    panic,
    poison,
    stun,
  }: Props) {
    this.name = name;
    this.bp = bp || 0;
    this.hp = hp || 0;
    this.pp = pp || 0;
    this.melee = melee || 0;
    this.ranged = ranged || 0;
    this.technique = technique || 0;
    this.floor = floor || 0;
    this.dmgRes = dmgRes || 0;
    this.burn = burn || 0;
    this.freeze = freeze || 0;
    this.shock = shock || 0;
    this.blind = blind || 0;
    this.panic = panic || 0;
    this.poison = poison || 0;
    this.stun = stun || 0;
  }
}

